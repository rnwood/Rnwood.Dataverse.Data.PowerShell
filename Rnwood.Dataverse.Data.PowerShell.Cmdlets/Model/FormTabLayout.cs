namespace Rnwood.Dataverse.Data.PowerShell.Model
{
    /// <summary>
    /// Represents the column layout options for a Dataverse form tab.
    /// </summary>
    public enum FormTabLayout
    {
        /// <summary>
        /// Single column layout (100% width).
        /// </summary>
        OneColumn,

        /// <summary>
        /// Two column layout (50% each).
        /// </summary>
        TwoColumns,

        /// <summary>
        /// Three column layout (33.33% each).
        /// </summary>
        ThreeColumns
    }
}